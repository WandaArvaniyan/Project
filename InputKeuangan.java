    private void kode() {
        try{
        Connection c = koneksi.getKoneksi();
        Statement s = c.createStatement();
        String sql = "SELECT * FROM keuangan ORDER BY id_keuangan DESC";
        ResultSet rs = s.executeQuery(sql); 
        
        if(rs.next()){
            String NoID = rs.getString("id_keuangan").substring(3);
            String ID = "" + (Integer.parseInt(NoID)+1);
            String Zero = "";
            
            if (ID.length()==1)
            {Zero = "00";}
            else if (ID.length()==2)
            {Zero = "0";}
            else if (ID.length()==3)
            {Zero = "";}
            
            txid.setText("K" + Zero + ID);
        }else{
            txid.setText("K001");
        }
        rs.close();
        s.close();
        }catch(Exception e){
            System.out.println("kode error");
        }
    }
