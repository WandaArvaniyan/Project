    public void tampiltabel(){
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("ID Keuangan");
        tb.addColumn("Tanggal");
        tb.addColumn("Pemasukan");
        tb.addColumn("Pengeluaran");
        tableKeuangan.setModel(tb);
        try{
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM keuangan";
            ResultSet rs = s.executeQuery(sql);
            
            while(rs.next()){
                tb.addRow(new Object[]{
                    rs.getString("id_keuangan"), 
                    rs.getString("tanggal"),
                    rs.getString("pemasukan"),
                    rs.getString("pengeluaran"),
                        
                });
                
            }
            
        }catch(Exception e){
            System.out.println("keuangan error");
        }
    }
