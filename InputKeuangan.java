    private void txcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txcariActionPerformed
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "select * from keuangan where id_keuangan like '%" + txcari.getText() + "%' or tanggal like'%" + txcari.getText() + "%' or pemasukan like'" + txcari.getText() + "%' or pengeluaran like'%" + txcari.getText() + "%'";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                Object[] o = new Object[5];
                o[0] = r.getString("id_keuangan");
                o[1] = r.getString("tanggal");
                o[2] = r.getString("pemasukan");
                o[3] = r.getString("pengeluaran");                
                
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }
    }//GEN-LAST:event_txcariActionPerformed

